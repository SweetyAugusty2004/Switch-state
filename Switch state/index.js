var day= prompt("please enter a day");
switch(day){
    case "Monday":
        alert("Start of the week");
        break;
        case "Tuesday":
        alert("Keep going");
        break;
        case "Wednesday":
        alert("Midweek already");
        break;
        case "Thursday":
        alert("Almost there");
        break;
        case "Friday":
        alert("Last day of the work week");
        break;
        case "Saturday":
        alert("It's the weekend! Enjoy!");
        break;
        case "Sunday":
        alert("Rest & Recharge for the week ahead");
        break;
        default:
        alert("nvalid day entered");
        break;
}